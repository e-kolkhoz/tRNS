#ifndef DIGITAL_POT_H
#define DIGITAL_POT_H

#include <Arduino.h>
#include "config.h"

// ============================================================================
// === X9C103S DIGITAL POTENTIOMETER ===
// ============================================================================
// X9C103S - 10kΩ цифровой потенциометр с EEPROM
// - 100 позиций (0-99)
// - Энергонезависимая память (100,000 циклов)
// - Запись в EEPROM при CS: LOW→HIGH
// - НЕЛЬЗЯ прочитать текущую позицию программно!

// Инициализация цифрового потенциометра (устанавливает на максимум для безопасности)
void initDigitalPot();

// Установка позиции потенциометра (0-99, с сохранением в EEPROM)
// 0 = минимальное сопротивление, 99 = максимальное
void setDigitalPotPosition(int target_position);

// Получить текущую позицию (программное отслеживание, читать из чипа нельзя!)
int getDigitalPotPosition();

#endif // DIGITAL_POT_H

